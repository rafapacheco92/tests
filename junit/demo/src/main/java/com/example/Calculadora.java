package com.example;

public class Calculadora {
  public Integer soma(Integer a, Integer b) {
    return a + b;
  }

  public Integer sub(Integer a, Integer b) {
    return a - b;
  }

  public Integer mult(Integer a, Integer b) {
    return a * b;
  }

  public Integer div(Integer a, Integer b) {
    return a / b;
  }
}
